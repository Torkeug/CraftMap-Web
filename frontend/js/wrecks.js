/* Wrecks screen: a browsable tree of shipwreck rare-loot-crate odds, typed
 * search narrowing/auto-expanding it - not a "pick one, see its own detail
 * panel" combo, since with only ~18 sectors / ~150 items total (see
 * backend/shipwreck_loot.py's own module docstring) the whole dataset is
 * cheap to hold client-side and browse directly, same as js/deposits.js's
 * own tree (whose tree-node/tree-row/disclosure/tree-children/tree-label
 * classes this reuses for a consistent look).
 *
 * Two modes, toggled by the mode tabs (mirrors js/recipe-panel.js's
 * Breakdown/Totals/Used-In tabs), sharing the same search box/tree:
 *   - "By Sector": Sector > loot-level header > item, for "I'm here right
 *     now, what can I get" browsing.
 *   - "By Item": Category > item > sector, for "I want X, where do I farm
 *     it" - per game_data_extract/shipwreck_loot_integration.md's original
 *     "Open questions" (itemDropOdds was already shaped for this).
 * The Patch/Blueprint checkboxes filter both. The search box matches
 * either the top-level entity's own name (sector name in Sector mode, item
 * name in Item mode) - in which case ALL of its children show - or a leaf
 * name (item name in Sector mode, sector name in Item mode), in which case
 * only matching leaves show; either way, matched branches force-expand
 * regardless of manual collapse state so results are visible without
 * clicking through the tree first.
 *
 * Read-only by design, same rationale as js/sources.js: this data is
 * derived from the game's own files (game_data_extract/shipwreck_loot.json,
 * regenerated by extract_shipwreck_loot.py), not hand-maintained.
 */
(function () {
  const modeSectorBtn = document.getElementById("wrecks-mode-sector");
  const modeItemBtn = document.getElementById("wrecks-mode-item");
  const modeLiveBtn = document.getElementById("wrecks-mode-live");
  const searchInput = document.getElementById("wrecks-search");
  const searchRowEl = document.getElementById("wrecks-search-row");
  const treeEl = document.getElementById("wrecks-tree");
  const filterPatch = document.getElementById("wrecks-filter-patch");
  const filterBlueprint = document.getElementById("wrecks-filter-blueprint");
  const liveViewEl = document.getElementById("wrecks-live-view");

  let mode = "sector"; // "sector" | "item" | "live" - session-only, not persisted
  let sectorsData = null; // fetched once per mode, cached
  let itemsData = null;
  // Session-only, not persisted (unlike js/deposits.js's collapsed_nodes,
  // which is pushed to config.json) - a key's PRESENCE means expanded;
  // everything starts collapsed by default (browse the outline first),
  // and an active search force-expands matching branches regardless of
  // this set, without mutating it, so manual state survives clearing the
  // search box.
  const expandedKeys = new Set();

  function fmtPct(pct) {
    const rounded = Math.round(pct * 100) / 100;
    return `${rounded}%`;
  }

  // itemDropOdds's pct is conditional on a crate already being open;
  // wreckSiteItemOdds's atLeastOnePct folds in the sector's own crate-count
  // mix (Big wrecks average ~4x more crates than Small - see
  // shipwreck_loot_integration.md), so pairing them answers both "given I
  // find a crate" and "walking this whole wreck site". atLeastOnePct is
  // missing (null) for the handful of items marked unobtainable, which
  // backend/shipwreck_loot.py never gives a wreckSiteItemOdds entry at all.
  function fmtOdds(pct, atLeastOnePct) {
    const crateText = fmtPct(pct);
    return atLeastOnePct == null ? crateText : `${crateText} · ${fmtPct(atLeastOnePct)}/site`;
  }

  function matchesQuery(text, q) {
    return !q || text.toLowerCase().includes(q);
  }

  // "avg Y/Big wreck, Z/Small wreck" - see the crate_spawn_by_size docstring
  // in backend/shipwreck_loot.py for why this is shown per size rather than
  // as one sector-blended figure. Uses "total" (debris field + the hull
  // piece's own secondary generation pass - always present, not tied to
  // any player action, see that same docstring) since that's the real
  // total for the wreck, not just what's scattered loose around it -
  // falls back to "n/a" for a sector whose generation table has no
  // variant of a given size at all (rare, but crate_spawn_by_size only
  // ever has keys for sizes present).
  function formatAvgBySize(bySize) {
    const parts = [];
    for (const size of ["Big", "Small"]) {
      const stats = bySize && bySize[size];
      if (stats) parts.push(`${stats.total.expected_count.toFixed(2)}/${size} wreck`);
    }
    return parts.length ? `avg ${parts.join(", ")}` : "";
  }

  // ---- node builders ----
  function makeGroupNode(key, label, tagClass, rightText) {
    return { kind: "group", key, label, tagClass, rightText, children: [] };
  }
  function makeLeafNode(label, tagClass, rightText, catBadge) {
    return { kind: "leaf", label, tagClass, rightText, catBadge };
  }
  function makeSummaryNode(label, tagClass) {
    return { kind: "summary", label, tagClass };
  }

  // ---- DOM rendering (mirrors js/deposits.js's renderNode/toggleNode -
  // same structural classes, plus a right-aligned value column and an
  // optional category badge these rows need that the deposit tree
  // doesn't) ----
  function renderTreeNode(node, forceExpand) {
    const wrapper = document.createElement("div");
    const row = document.createElement("div");
    row.className = "tree-row";
    const spacer = document.createElement("span");
    spacer.className = "disclosure";

    const mainEl = document.createElement("span");
    mainEl.className = "tree-row-main";
    if (node.catBadge) {
      const catEl = document.createElement("span");
      catEl.className = `wrecks-row-cat wrecks-row-cat-${node.catBadge}`;
      catEl.textContent = node.catBadge === "blueprint" ? "BP" : "Patch";
      mainEl.appendChild(catEl);
    }
    const labelEl = document.createElement("span");
    labelEl.className = `tree-label ${node.tagClass || ""}`;
    labelEl.textContent = node.label;
    mainEl.appendChild(labelEl);

    row.appendChild(spacer);
    row.appendChild(mainEl);

    if (node.rightText) {
      const rightEl = document.createElement("span");
      rightEl.className = "wrecks-row-pct";
      rightEl.textContent = node.rightText;
      row.appendChild(rightEl);
    }

    if (node.kind === "group") {
      wrapper.className = "tree-node";
      const collapsed = !forceExpand && !expandedKeys.has(node.key);
      if (collapsed) wrapper.classList.add("collapsed");
      row.classList.add("group");
      spacer.textContent = "▸";
      row.addEventListener("click", () => toggleNode(wrapper, node.key));
      wrapper.appendChild(row);

      const childrenEl = document.createElement("div");
      childrenEl.className = "tree-children";
      for (const child of node.children) {
        childrenEl.appendChild(renderTreeNode(child, forceExpand));
      }
      wrapper.appendChild(childrenEl);
    } else if (node.kind === "leaf") {
      wrapper.className = "tree-node leaf";
      wrapper.appendChild(row);
    } else {
      wrapper.className = "tree-node summary";
      wrapper.appendChild(row);
    }

    return wrapper;
  }

  function toggleNode(wrapper, key) {
    const collapsed = wrapper.classList.toggle("collapsed");
    if (collapsed) expandedKeys.delete(key);
    else expandedKeys.add(key);
  }

  function makeMessageRow(text) {
    return renderTreeNode(makeSummaryNode(text), false);
  }

  // ---- Sector mode: Sector > (summary lines) > loot-level header > item ----
  function buildSectorTree(q) {
    const showPatch = filterPatch.checked;
    const showBlueprint = filterBlueprint.checked;
    const roots = [];
    for (const sector of sectorsData) {
      const items = sector.items.filter((it) =>
        it.category === "blueprint" ? showBlueprint : showPatch
      );
      const sectorMatches = matchesQuery(sector.name, q);
      const visibleItems = sectorMatches
        ? items
        : items.filter((it) => matchesQuery(it.name, q));
      if (q && visibleItems.length === 0) continue;

      // Item levels actually obtainable here (not sector.loot_level_probability's
      // own crate-TARGET-level distribution, shown below in the summary line -
      // see the loot-level divider's own comment further down for why those
      // two aren't the same number): the 2-level eligibility window means
      // this can span one level below the lowest crate target.
      const itemLevels = items.map((it) => it.level);
      const levelRangeText = itemLevels.length
        ? Math.min(...itemLevels) === Math.max(...itemLevels)
          ? `L${itemLevels[0]}`
          : `L${Math.min(...itemLevels)}-L${Math.max(...itemLevels)}`
        : "";
      // avg crates/wreck on the row itself (not just the expanded "Rare
      // crate odds" summary below) so it's visible without expanding. Shown
      // per SIZE, not the blended sector average: a Big wreck (BigPiece1/2
      // + SmallPiece1/2 debris hull) runs ~4x a Small wreck's own crate
      // count (backend/shipwreck_loot.py's get_all_sectors docstring), so
      // quoting one blended number understates Big and overstates Small -
      // whichever wreck you're actually standing at, the size-specific
      // number is the one that applies.
      const avgCrateText = formatAvgBySize(sector.crate_spawn_by_size);
      const rowRightText = [levelRangeText, avgCrateText].filter(Boolean).join(" · ");

      const sectorNode = makeGroupNode(
        `wsec|${sector.name}`,
        sector.name,
        "sector",
        rowRightText
      );
      const crateTargets = Object.entries(sector.loot_level_probability)
        .sort((a, b) => Number(a[0]) - Number(b[0]))
        .map(([lvl, p]) => `L${lvl} ${fmtPct(p * 100)}`)
        .join(", ");
      sectorNode.children.push(
        makeSummaryNode(
          `Explo ${sector.explo_level} · max loot ${sector.max_loot_level} · crate targets: ${crateTargets}`,
          "loc_sum"
        )
      );
      // Whether a wreck HAS a rare loot crate at all, not what's in one
      // given it exists (that's crateTargets/the items below) - a single
      // wreck can hold more than one, hence "avg X/wreck" alongside the
      // at-least-one %. Broken out per SIZE (Big vs Small), not the
      // sector-blended average - see backend.shipwreck_loot.get_all_sectors's
      // own docstring for why the blend understates/overstates depending on
      // which size wreck you're actually at. Each size also splits the
      // scattered debris field out from the FULL wreck total (its marked
      // hull piece always triggers a SECOND, independent generation pass
      // too - unconditional, not tied to any player action - same
      // docstring) - shown as two lines since "just the loose debris" and
      // "everything this wreck has" are both meaningful numbers on their
      // own, not two player-behavior scenarios to pick between.
      for (const size of ["Big", "Small"]) {
        const stats = sector.crate_spawn_by_size && sector.crate_spawn_by_size[size];
        if (!stats) continue;
        sectorNode.children.push(
          makeSummaryNode(
            `Rare crate odds (${size} wreck, debris field only): ` +
              `${fmtPct(stats.at_least_one * 100)} chance of ≥1 (avg ${stats.expected_count.toFixed(2)}/wreck)`,
            "loc_sum"
          )
        );
        const total = stats.total;
        sectorNode.children.push(
          makeSummaryNode(
            `Rare crate odds (${size} wreck, full site): ` +
              `${fmtPct(total.at_least_one * 100)} chance of ≥1 (avg ${total.expected_count.toFixed(2)}/wreck)`,
            "loc_sum"
          )
        );
      }
      sectorNode.children.push(
        makeSummaryNode(
          `Secondary materials: ${sector.secondary_material_pool.join(", ")}`,
          "loc_sum"
        )
      );

      // Grouped by loot level as a plain divider, not a nested collapsible
      // group - see the divider's own note in the item loop below for why
      // it's not annotated with a crate-odds percentage.
      let currentLevel = null;
      for (const item of visibleItems) {
        if (item.level !== currentLevel) {
          currentLevel = item.level;
          sectorNode.children.push(makeSummaryNode(`Loot level ${currentLevel}:`, "loc_sum"));
        }
        sectorNode.children.push(
          makeLeafNode(
            item.name,
            "planet",
            fmtOdds(item.pct, item.at_least_one_pct),
            item.category
          )
        );
      }
      roots.push(sectorNode);
    }
    return roots;
  }

  // ---- Item mode: Category > item > sector ----
  function buildItemTree(q) {
    const categories = [
      { key: "patch", label: "Patches", show: filterPatch.checked },
      { key: "blueprint", label: "Blueprints", show: filterBlueprint.checked },
    ];
    const roots = [];
    for (const cat of categories) {
      if (!cat.show) continue;
      const catNode = makeGroupNode(`wcat|${cat.key}`, cat.label, "type");
      let anyChild = false;

      for (const item of itemsData.filter((it) => it.category === cat.key)) {
        const itemMatches = matchesQuery(item.name, q);
        const sectorRows = [];
        for (const group of item.groups) {
          for (const sector of group.sectors) {
            sectorRows.push({
              sectorName: sector.name,
              pct: group.pct,
              atLeastOnePct: sector.at_least_one_pct,
            });
          }
        }
        const visibleRows = itemMatches
          ? sectorRows
          : sectorRows.filter((r) => matchesQuery(r.sectorName, q));
        if (q && visibleRows.length === 0) continue;
        anyChild = true;

        const itemNode = makeGroupNode(`witem|${item.name}`, item.name, "resource");
        const bestSiteText =
          item.best_at_least_one_pct == null
            ? ""
            : ` · best ${fmtPct(item.best_at_least_one_pct)} per wreck site`;
        itemNode.children.push(
          makeSummaryNode(
            item.obtainable
              ? `Loot level ${item.level} · best odds ${fmtPct(item.best_pct)} per rare loot crate${bestSiteText}`
              : `Loot level ${item.level} · not obtainable from any wreck crate`,
            "loc_sum"
          )
        );
        visibleRows.sort((a, b) => b.pct - a.pct || a.sectorName.localeCompare(b.sectorName));
        for (const r of visibleRows) {
          itemNode.children.push(
            makeLeafNode(r.sectorName, "loc_sec", fmtOdds(r.pct, r.atLeastOnePct))
          );
        }
        catNode.children.push(itemNode);
      }

      if (q && !anyChild) continue;
      roots.push(catNode);
    }
    return roots;
  }

  // ---- shared render/filter ----
  function render() {
    const q = searchInput.value.trim().toLowerCase();
    treeEl.innerHTML = "";

    if (!filterPatch.checked && !filterBlueprint.checked) {
      treeEl.appendChild(makeMessageRow("No categories selected."));
      return;
    }
    const roots = mode === "sector" ? buildSectorTree(q) : buildItemTree(q);
    if (!roots.length) {
      treeEl.appendChild(makeMessageRow("No matches."));
      return;
    }
    const forceExpand = q.length > 0;
    for (const node of roots) {
      treeEl.appendChild(renderTreeNode(node, forceExpand));
    }
  }

  async function ensureDataLoaded() {
    if (mode === "sector" && sectorsData === null) {
      sectorsData = await CraftMapApi.call("get_wreck_sectors");
    } else if (mode === "item" && itemsData === null) {
      itemsData = await CraftMapApi.call("get_wreck_items");
    }
  }

  async function setMode(newMode) {
    if (mode === newMode) return;
    mode = newMode;
    modeSectorBtn.classList.toggle("active", mode === "sector");
    modeItemBtn.classList.toggle("active", mode === "item");
    modeLiveBtn.classList.toggle("active", mode === "live");
    searchRowEl.classList.toggle("hidden", mode === "live");
    treeEl.classList.toggle("hidden", mode === "live");
    liveViewEl.classList.toggle("hidden", mode !== "live");
    if (mode === "live") {
      await LiveTracking.onShow();
      return;
    }
    LiveTracking.onHide();
    searchInput.placeholder = mode === "sector" ? "Filter sectors..." : "Filter items...";
    await ensureDataLoaded();
    render();
  }

  // ---- Live Tracking sub-tab: SETUP (script path) + HISTORICAL DATA
  // (all-time sightings stats) only - the live, per-cycle current-planet
  // view with bearing/distance lives in its own separate pinnable window
  // (frontend/wreck-tracker.html/js/wreck-tracker-panel.js, opened by
  // Activate below), not here, so it keeps working/visible even while
  // this tab isn't the active screen. Still polls get_live_wreck_snapshot
  // itself (status text only, snapshot body unused) - that call also
  // piggybacks a wreck_events import as a side effect (see
  // Api.get_live_wreck_snapshot's own docstring), which needs to keep
  // happening for the stats below to stay current regardless of whether
  // the separate HUD window is even open. Wrapped in its own object
  // (rather than more top-level functions in this closure) since it owns
  // a setInterval poll loop with clean start/stop lifecycle tied to
  // onShow/onHide, distinct from the static-data render() above. ----
  const LiveTracking = (function () {
    const scriptPathInput = document.getElementById("wrecks-live-script-path");
    const pythonPathInput = document.getElementById("wrecks-live-python-path");
    const saveSettingsBtn = document.getElementById("wrecks-live-save-settings-btn");
    const toggleBtn = document.getElementById("wrecks-live-toggle-btn");
    const statusEl = document.getElementById("wrecks-live-status");
    const statsEl = document.getElementById("wrecks-live-stats");
    const statsGroupRadios = document.querySelectorAll('input[name="wrecks-stats-group"]');

    const POLL_MS = 3000;
    let pollTimer = null;
    let tracking = false; // this tab's own belief that a start was requested
    let sawFirstSnapshot = false;

    function fmtTime(iso) {
      if (!iso) return "";
      const d = new Date(iso);
      return isNaN(d) ? "" : d.toLocaleTimeString();
    }

    function setStatus(text, cls) {
      statusEl.textContent = text;
      statusEl.className = cls || "";
    }

    function makeRow(mainText, rightText) {
      const row = document.createElement("div");
      row.className = "source-row";
      const nameEl = document.createElement("span");
      nameEl.className = "source-row-name";
      nameEl.textContent = mainText;
      row.appendChild(nameEl);
      if (rightText) {
        const rightEl = document.createElement("span");
        rightEl.className = "wrecks-live-row-right";
        rightEl.textContent = rightText;
        row.appendChild(rightEl);
      }
      return row;
    }

    function currentStatsGroup() {
      for (const r of statsGroupRadios) if (r.checked) return r.value;
      return "planet";
    }

    async function renderStats() {
      const rows = await CraftMapApi.call("get_wreck_stats");
      statsEl.innerHTML = "";
      if (!rows.length) {
        statsEl.appendChild(makeRow("No sightings logged yet."));
        return;
      }
      const groupBy = currentStatsGroup();
      const groups = new Map(); // groupLabel -> {display_name -> {seen, looted, despawned}}
      for (const r of rows) {
        const label =
          groupBy === "sector"
            ? r.sector || "(unknown sector)"
            : groupBy === "system"
              ? r.system_name
              : `${r.system_name} - ${r.planet}`;
        const byResource = groups.get(label) || new Map();
        const key = `${r.display_name} L${r.level}`;
        const agg = byResource.get(key) || { seen: 0, looted: 0, despawned: 0 };
        agg.seen += r.seen_count;
        agg.looted += r.looted_count;
        agg.despawned += r.despawned_count;
        byResource.set(key, agg);
        groups.set(label, byResource);
      }
      for (const [label, byResource] of [...groups.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
        const header = document.createElement("div");
        header.className = "source-row source-row-header";
        header.textContent = label;
        statsEl.appendChild(header);
        for (const [resLabel, agg] of [...byResource.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
          const parts = [`${agg.seen} seen`];
          if (agg.looted) parts.push(`${agg.looted} looted`);
          if (agg.despawned) parts.push(`${agg.despawned} despawned`);
          statsEl.appendChild(makeRow(`  ${resLabel}`, parts.join(" · ")));
        }
      }
    }

    // The live snapshot FILE persists on disk across poller restarts - a
    // freshly (re)started poller's get_live_wreck_snapshot immediately
    // returns a previous session's leftover the instant it's called,
    // well before the new poller has actually attached/written anything
    // of its own (attach alone can take 1-3 minutes cold - see
    // wreck_tracker.py's own module docstring). Without checking the
    // timestamp's actual age, that leftover reads as current ("Running -
    // updated <plausible old time>") instead of "still starting up" -
    // confirmed live: this exact confusion happened, a stale
    // not-in-planet snapshot from a dead poller instance was shown as
    // if it were this session's real status. Threshold generous
    // relative to normal cycle time but far shorter than any realistic
    // attach time.
    const STALE_THRESHOLD_MS = 5000;

    function isFresh(snapshot) {
      if (!snapshot) return false;
      return Date.now() - new Date(snapshot.observed_at).getTime() < STALE_THRESHOLD_MS;
    }

    async function poll() {
      let snapshot = null;
      try {
        snapshot = await CraftMapApi.call("get_live_wreck_snapshot");
      } catch (e) {
        // CraftMapApi.call already surfaces this via the error banner
      }
      if (isFresh(snapshot)) {
        sawFirstSnapshot = true;
        setStatus(`Running - updated ${fmtTime(snapshot.observed_at)}`, "running");
      } else if (!sawFirstSnapshot) {
        setStatus("Starting up, scanning game memory (can take 1-3 minutes the first time)...", "starting");
      }
      const status = await CraftMapApi.call("get_wreck_tracking_status");
      if (!status.running && tracking) {
        // Subprocess died on its own (game not running, script path
        // wrong, etc.) - reflect that rather than keep claiming "starting".
        tracking = false;
        sawFirstSnapshot = false;
        setStatus("Tracker stopped unexpectedly - check the script path and that the game is running.", "");
        toggleBtn.textContent = "Activate Live Tracking";
        stopPolling();
      }
      await renderStats();
    }

    function startPolling() {
      if (pollTimer) return;
      poll();
      pollTimer = setInterval(poll, POLL_MS);
    }

    function stopPolling() {
      if (pollTimer) {
        clearInterval(pollTimer);
        pollTimer = null;
      }
    }

    async function toggleTracking() {
      if (tracking) {
        await CraftMapApi.call("stop_wreck_tracking");
        tracking = false;
        sawFirstSnapshot = false;
        stopPolling();
        setStatus("Not running.", "");
        toggleBtn.textContent = "Activate Live Tracking";
        // The Wreck Tracker window has no close button of its own (see
        // wreck-tracker.html's own comment) - its lifecycle is tied
        // 1:1 to tracking state, so stopping tracking is what closes it.
        CraftMapApi.call("hide_wreck_tracker_window");
        return;
      }
      try {
        await CraftMapApi.call("start_wreck_tracking");
      } catch (e) {
        return; // error banner already shown by CraftMapApi.call
      }
      tracking = true;
      sawFirstSnapshot = false;
      toggleBtn.textContent = "Stop Live Tracking";
      setStatus("Starting up, scanning game memory (can take 1-3 minutes the first time)...", "starting");
      startPolling();
      // Opens (never hides) the separate Wreck Tracker HUD window - same
      // "always shows" semantics as the recipe panel's own '+ Queue'
      // button (see Api.show_wreck_tracker_window's own docstring). The
      // window itself keeps polling independently once open, same as the
      // queue window does while the main window is hidden.
      CraftMapApi.call("show_wreck_tracker_window");
    }

    async function saveSettings() {
      await CraftMapApi.call(
        "set_wreck_tracker_settings",
        scriptPathInput.value.trim(),
        pythonPathInput.value.trim()
      );
    }

    async function loadSettings() {
      const settings = await CraftMapApi.call("get_wreck_tracker_settings");
      scriptPathInput.value = settings.script_path || "";
      pythonPathInput.value = settings.python_path || "";
    }

    async function onShow() {
      const status = await CraftMapApi.call("get_wreck_tracking_status");
      tracking = status.running;
      if (tracking) {
        toggleBtn.textContent = "Stop Live Tracking";
        startPolling();
      } else {
        toggleBtn.textContent = "Activate Live Tracking";
        setStatus("Not running.", "");
      }
      await renderStats();
    }

    function onHide() {
      stopPolling();
    }

    function init() {
      loadSettings();
      saveSettingsBtn.addEventListener("click", saveSettings);
      toggleBtn.addEventListener("click", toggleTracking);
      for (const r of statsGroupRadios) r.addEventListener("change", renderStats);
    }

    return { init, onShow, onHide };
  })();

  async function init() {
    await ensureDataLoaded();
    render();
    modeSectorBtn.addEventListener("click", () => setMode("sector"));
    modeItemBtn.addEventListener("click", () => setMode("item"));
    modeLiveBtn.addEventListener("click", () => setMode("live"));
    searchInput.addEventListener("input", render);
    filterPatch.addEventListener("change", render);
    filterBlueprint.addEventListener("change", render);
    LiveTracking.init();
  }

  init();
})();
